    private void convertBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_convertBtnMouseClicked
        // TODO add your handling code here:
        try{
        int c = currency.getSelectedIndex();
        double php = Double.parseDouble(pesoTxt.getText());
        double con = 0;
        switch(c){
            case 0:
                con = php * 0.018969;
            break;
            case 1:
                con = php * 0.016893;
            break;
            case 2:
                con = php * 0.014532;
            break;
            case 3:
                con = php * 1.314371;
            break;
            case 4:
                con = php * 0.026788;
            break;
            case 5:
                con = php * 0.025479;
            break;
            case 6:
                con = php * 0.025728;
            break;
            case 7:
                con = php * 0.018873;
            break;
            case 8:
                con = php * 0.077354;
            break;
            case 9:
                con = php * 2.098890;
            break;
            
        }
        
        resTxt.setText(Double.toString(con));
        }catch(Exception e){
            resTxt.setText("INPUT ERROR");
        }
        
    }//GEN-LAST:event_convertBtnMouseClicked

    
    

