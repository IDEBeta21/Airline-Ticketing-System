    public viewRecord(String fileName,String counName,String contName,String distance) {
        initComponents();
        
        this.dircc=fileName;
        this.counname=counName;
        this.contname=contName;
        this.d = distance;
        
        try {
            String nameOfFile = fileName;
            Scanner scan = new Scanner(new File(nameOfFile));
            
            int numOfSeats=0;
            
            
            while(scan.hasNext()){
              //display.setText(display.getText()+"\n"+scan.nextLine());
              
                String id = scan.next();
                
                String rSeatNum     = scan.next();
                String rSeatClass   = scan.next().replace("_"," ");
                String rlname       = scan.next().replace("_"," ");
                String rfname       = scan.next().replace("_"," ");
                String rmname       = scan.next().replace("_"," ");
                String rcontinent   = scan.next().replace("_"," ");
                String rcountry     = scan.next().replace("_"," ");
                String rairline     = scan.next().replace("_"," ");
                String rdate        = scan.next().toString();
                String rtime        = scan.next().replace("_"," ");
                String rtraveltype  = scan.next().replace("_"," ");
                String rcost        = scan.next().toString();
                String rpay         = scan.next().toString();
                
                countryName.setText(counname+", "+contname);
                distanceDisplay.setText(d+" Miles");
                DefaultTableModel datas = (DefaultTableModel)RecordTable.getModel();
                datas.addRow(new Object[]{id, rSeatNum, rSeatClass, rlname, rfname, rmname, rcontinent, rcountry, rairline, rdate, rtime, rtraveltype, rcost, rpay});
                
                LineNumberReader record = new LineNumberReader(new FileReader(new File(fileName)));

                while ((record.readLine())!=null){
                    numOfSeats=record.getLineNumber();
                }
                
                resSeatDisplay.setText(Integer.toString(numOfSeats));
                avaSeatDisplay.setText(Integer.toString(30-numOfSeats));
                
            }
            
        } catch (FileNotFoundException ex) {
            //Logger.getLogger(viewRecord.class.getName()).log(Level.SEVERE, null, ex);
            
            
            
        } catch(IOException e){
            errorM errr = new errorM("File not Existed Yet");
            errr.setVisible(true);
            
        }
        
        currency.add("US Dollar");
        currency.add("Euro");
        currency.add("British Pound");
        currency.add("Indian Rupee");
        currency.add("Ausralian Dollar");
        currency.add("Canadian Dollar");
        currency.add("Singapore Dollar");
        currency.add("Swiss Franc");
        currency.add("Malaysian Ringgit");
        currency.add("Japanese Yen");
        
    }

    
